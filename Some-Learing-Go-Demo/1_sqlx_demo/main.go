package main

import (
	"errors"
	"fmt"

	"github.com/jmoiron/sqlx"

	_ "github.com/go-sql-driver/mysql" //驱动
)

type user struct {
	ID   int    `db:"id"`
	Name string `db:"name"`
	Age  int    `db:"age"`
}

var db *sqlx.DB

func initDB() (err error) {
	dsn := "root:1234@tcp(127.0.0.1:3306)/sqlx_01?" +
		"parseTime=true&" + // 解析时间类型为 time.Time
		"loc=Local&" + // 使用本地时区
		"charset=utf8mb4" // 使用 UTF-8 编码

		//连接
	db, err = sqlx.Connect("mysql", dsn)
	if err != nil {
		fmt.Printf("Connect db failed,err:%v\n", err)
		return
	}
	db.SetMaxOpenConns(20) //最大连接数
	db.SetMaxIdleConns(10) //最大空闲连接数
	return
}

// 查询单条数据示例
